        return UIColor(red:0 / 255.0, green:122 / 255.0, blue:255 / 255.0, alpha:1.0)
    }
    
    static func greenExecutionTimeColor() -> UIColor {
        return UIColor(red:50 / 255.0, green:189 / 255.0, blue:40 / 255.0, alpha:1.0)
    }
    
    static func redExecutionTimeColor() -> UIColor {
        return UIColor(red:220 / 255.0, green:47 / 255.0, blue:52 / 255.0, alpha:1.0)
    }
}